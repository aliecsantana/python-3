# O tipo de dado bool (boolean) serve para questionar algo em um programa e só existem duas respostas possíveis: sim (True) ou não (False)

# Exemplo de comparação com o operador lógico ==
print(10 == 10)  # True (sim)
print(10 == 11)  # False (não)

# Verificando os tipos de dados de várias expressões
print(type(True))   # <class 'bool'>
print(type(False))  # <class 'bool'>
print(type(10 == 10))  # <class 'bool'>
print(type(10 == 11))  # <class 'bool'>